thing = "HELLO world HERE is SOME text"
# Only print lowercase function
print(thing.lower())
# Only print uppercase function
print(thing.upper())
# Capitalize first letter function
print(thing.capitalize())
# Capitalize everything function
print(thing.title())
# Check if the entire string has digits function
print(thing.isdigit())
# See index and rindex function
print(thing.find("world"))
print(thing.count("E"))
# Reverse a string function (palindrome)
print(thing[::-1])
# Split up strings function
print(thing.split(" "))
# Print string multiple times
print("Hello" * 5)

